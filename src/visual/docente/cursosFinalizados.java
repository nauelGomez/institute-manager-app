    private void habilitarCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_habilitarCursoActionPerformed
        int filaSeleccionada = tableCursosFinalizados.getSelectedRow();;
        String cid = modeloTabla.getValueAt(filaSeleccionada, 0).toString();
        int id = Integer.parseInt(cid);
        ArrayList<Curso> cursos = instituto.getCursos();
        
        for(Curso curso : cursos)
        {
            if(id == curso.getId()){
                Curso cursoCopia = new Curso(curso.getDocente(), curso.getEstado(), curso.getTopeAlumnos(), curso.getRequisitos());
                this.userDocente.habilitarCurso(cursoCopia);
                break;
            }
        }
        modeloTabla.setRowCount(0);
        cargarTabla(modeloTabla);      
        
    }//GEN-LAST:event_habilitarCursoActionPerformed


 private static void setUnderline(JLabel label, boolean underline) {
        Font font = label.getFont();
        Map<TextAttribute, Object> attributes = new HashMap<>(font.getAttributes());
        attributes.put(TextAttribute.UNDERLINE, underline ? TextAttribute.UNDERLINE_ON : null);
        label.setFont(font.deriveFont(attributes));
    }
 
  public ArrayList<Curso> obtenerCursosPorEstado(String estado)
  {
      ArrayList<Curso> cursosTotales = instituto.getCursos();
      ArrayList<Curso> cursosCerradosDelDocente = new ArrayList<Curso>();
      
      for(Curso curso : cursosTotales)
      {
          if(curso.getDocente() == this.userDocente && curso.getEstado().equals(estado))
          {
              cursosCerradosDelDocente.add(curso);
          }
      }
      
      return cursosCerradosDelDocente;
  }
  

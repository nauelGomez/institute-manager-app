    private void btnProponerCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProponerCursoActionPerformed
        
    String descripcion = descTextField.getText();
String topeAlumnos = CantidadAlumnos.getText();
List<String> seleccionados = listaCursos.getSelectedValuesList();
String[] arraySeleccionados = seleccionados.toArray(new String[0]);
ArrayList<Curso> cursos = instituto.getCursos();
ArrayList<Curso> cursosSeleccionados = new ArrayList<>();
int topeAlumnosInt = 0; // Inicializar a 0
try {
    topeAlumnosInt = Integer.parseInt(topeAlumnos);
} catch (NumberFormatException e) {
    // Manejar la excepción si no se puede convertir a entero
    new JDialogInfo(this, true, "La cantidad de alumnos debe ser un número válido.").setVisible(true);
    return; // Salir del método porque no se puede continuar sin la cantidad de alumnos válida
}

// Recorrer los cursos seleccionados en el JList
for (String seleccionado : arraySeleccionados) {
    // Buscar el curso por su descripción
    for (Curso curso : cursos) {
        if (curso.getDescripcion().equals(seleccionado)) {
            cursosSeleccionados.add(curso);
            break; // Terminar el bucle interno cuando se encuentre el curso
        }
    }
}

if (descripcion.isEmpty() || topeAlumnos.isEmpty()) {
    new JDialogInfo(this, true, "Por favor, complete todos los campos.").setVisible(true);
    return; // Salir del método porque faltan datos
}

if (cursosSeleccionados.size() > 2) {
    new JDialogInfo(this, true, "Seleccione como máximo 2 cursos.").setVisible(true);
    return; // Salir del método porque hay demasiadas correlativas seleccionadas
}

JDialogDeseaContinuar dialog = new JDialogDeseaContinuar(this, true);
dialog.setVisible(true);
int seleccion = dialog.getReturnStatus();
dialog.dispose(); // Cerrar el diálogo después de usarlo

if (seleccion == 1) {
    try {
        instituto.agregarCurso(userDocente.proponerCurso(cursosSeleccionados, topeAlumnosInt, descripcion, this.instituto));
        new JDialogInfo(this, true, "Curso propuesto exitosamente.").setVisible(true);
        this.dispose();
        new visualDocente(this.userDocente,this.instituto).setVisible(true);
    } catch (CloneNotSupportedException ex) {
        Logger.getLogger(proponerCurso.class.getName()).log(Level.SEVERE, null, ex);
        new JDialogInfo(this, true, "Error al proponer el curso. Por favor, inténtelo de nuevo.").setVisible(true);
    }
}
serializarInstituto();
    }//GEN-LAST:event_btnProponerCursoActionPerformed


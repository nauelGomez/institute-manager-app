    private void CargarModelo(){
        
        ArrayList<Curso> cursos = instituto.getCursos();
        ArrayList<Curso> cursosAprobados = new ArrayList();
        for(Curso curso : cursos)
        {
            if(!curso.getEstado().equals("propuesto"))
            {
                cursosAprobados.add(curso);
            }
            
        }
        for(Curso curso : cursosAprobados)
        {
            modelo.addElement(curso.getDescripcion());
        }
    }

    public void serializarInstituto()
    {
        try {
            FileOutputStream archivoSalida = new FileOutputStream("instituto_serializados.dat");
            ObjectOutputStream objetoSalida = new ObjectOutputStream(archivoSalida);
            objetoSalida.writeObject(instituto);
            objetoSalida.close();
            archivoSalida.close();
        }
        catch (IOException e) {
            e.printStackTrace();
        }
    }

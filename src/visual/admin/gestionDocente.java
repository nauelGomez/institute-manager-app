    public void cargarTabla(DefaultTableModel modeloTabla)
    {
        ArrayList<Docente> listaDocentes = instituto.getDocentes();
        
        // Agregar filas al modelo de tabla a partir de los datos de los docentes
        for (Docente docente : listaDocentes) {
            String estado = "habilitado";
            if(docente.getSancion() != null)
            {
                estado = "<< inhabilitado >>";
            }
            Object[] fila = {docente.getDni(), docente.getNombre(), docente.getApellido(), estado};
            modeloTabla.addRow(fila);
        }
        
    }
    
    
    private int obtenerFilaSeleccionada() {
        return tableListadoDocentes.getSelectedRow();
    }

    private ArrayList<Docente> obtenerDocentesBloqueados()
    {
        ArrayList<Docente> docentes = instituto.getDocentes();
        ArrayList<Docente> docentesBloqueados = new ArrayList();

        for (Docente docente : docentes) {
            if(docente.getSancion() != null)
            {
                docentesBloqueados.add(docente);
            }
        }
            return docentesBloqueados;
        
    }
    
    
    private Docente buscarDocentePorDNI(String dni) 
    {
        ArrayList<Docente> listaDocentes = instituto.getDocentes();

        for (Docente docente : listaDocentes) {
            if (docente.getDni().equals(dni)) {
                return docente;// Se encontr√≥ al docente, devuelve el objeto Docente
            }
        }

        return null; // No se encontr√≥ al docente con el DNI especificado
    }
    
   
    
    public void serializarAdmin()
    {
        try 
        {
            FileOutputStream archivoSalida = new FileOutputStream("admin_serializado.dat");
            ObjectOutputStream objetoSalida = new ObjectOutputStream(archivoSalida);
            objetoSalida.writeObject(this.admin);
            objetoSalida.close();
            archivoSalida.close();
        }
        catch (IOException e){ 
            e.printStackTrace();
        }
    }
     

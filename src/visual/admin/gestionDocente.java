    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        modeloTabla.setRowCount(0);
        
        String nombre = txtNombre.getText().toLowerCase();
        String apellido = txtApellido.getText().toLowerCase();
        String dni = txtDni.getText();
        
        ArrayList<Docente> docentes = instituto.getDocentes();
        
        for(Docente docente : docentes)
        {
             if(docente.getDni().equals(dni) || docente.getNombre().equals(nombre) || docente.getApellido().equals(apellido) ||
              (docente.getNombre().contains(nombre) && docente.getApellido().contains(apellido) && docente.getDni().contains(dni)))
            {
                String estado = "habilitado";
                if(docente.getSancion() != null)
                {
                    estado = "<< inhabilitado >>";
                }
                Object[] fila = {docente.getDni(), docente.getNombre(), docente.getApellido(), estado};
                modeloTabla.addRow(fila);
            }
        }
        
        txtNombre.setText("");
        txtApellido.setText("");
        txtDni.setText("");
    }//GEN-LAST:event_btnBuscarActionPerformed


    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        //Indices: 1 = docente ; 2 = alumno
        int indice = comboTipo.getSelectedIndex();
        
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String dni = txtDni.getText();
        
        // Validar nombre y apellido
        if (!validarNombreApellido(nombre) || !validarNombreApellido(apellido)) {
            new JDialogInfo(this, true, "Nombre o apellido inválido").setVisible(true);
            return; // No continuar con el registro si la validación falla
        }

        // Validar DNI
        if (!validarDNI(dni)) {
            new JDialogInfo(this, true, "Dni inválido").setVisible(true);
            return; // No continuar con el registro si la validación falla
        }

        
        if(indice == 0)
        {
            try
            {
                this.instituto = admin.registrarDocente(dni,nombre,apellido,this.instituto);
                new JDialogInfo(this, true, "Docente agregado correctamente.").setVisible(true);
                serializarInstituto();
            }
            catch(UsuarioExistenteException e)
            {
                new JDialogError(this,true,e).setVisible(true);
            }
        }
        else if(indice == 1)
        {
            try
            {
                admin.registrarAlumno(dni,nombre,apellido, this.instituto);
                new JDialogInfo(this, true, "Alumno agregado correctamente.").setVisible(true);
                serializarInstituto();
            }
            catch(UsuarioExistenteException e)
            {
                new JDialogError(this,true,e).setVisible(true);
            }
        }
        
    }//GEN-LAST:event_btnAgregarActionPerformed


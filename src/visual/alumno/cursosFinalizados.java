    public void cargarTabla(DefaultTableModel modeloTabla)
    {
        ArrayList<CursosFinalizados> cursos = userAlumno.getCursosFinalizados();

        for(CursosFinalizados curso : cursos)
        {
           Object[] fila = {curso.getCurso().getId(),curso.getCurso().getDescripcion(),curso.getCurso().getDocente().getApellido(),curso.getEstado()};
           modeloTabla.addRow(fila);
        }
    }
